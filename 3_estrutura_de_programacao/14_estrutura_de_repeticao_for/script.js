
for(let i = 0; i < 100; i = i + 3) {
    //a cada iteração o i soma com 3 o primeiro é 0 e soma com 2 = 2
    //o segundo soma i(0) + 3 = 3 e soma com 2 = 5 
    //terceiro soma i(3) + 3 = 6 e soma com 2 = 8 e assim vai..
    console.log(`A soma de i com 2 é: ${i + 2}`);

}
